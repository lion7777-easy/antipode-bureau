const sqlite3 = require('sqlite3').verbose();
const path = require('path');
const fs = require('fs');

const DB_PATH = path.join(__dirname, 'data', 'antipode.db');
const dataDir = path.join(__dirname, 'data');

if (!fs.existsSync(dataDir)) {
    fs.mkdirSync(dataDir, { recursive: true });
}

// 强制删除旧数据库
try {
    if (fs.existsSync(DB_PATH)) {
        fs.unlinkSync(DB_PATH);
        console.log('🗑️ 旧数据库已删除');
    }
} catch (e) {
    console.log('⚠️ 删除旧数据库失败，尝试重命名备份');
    try {
        fs.renameSync(DB_PATH, DB_PATH + '.backup');
        console.log('📁 旧数据库已重命名为 antipode.db.backup');
    } catch (e2) {
        console.log('❌ 无法删除或重命名旧数据库，请手动删除 data/antipode.db');
        process.exit(1);
    }
}

const db = new sqlite3.Database(DB_PATH);

// ============================================================
// 创建所有表（含唯一索引，防止重复）
// ============================================================
db.serialize(() => {
    db.run(`
        CREATE TABLE IF NOT EXISTS cities (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            name_cn TEXT NOT NULL,
            name_en TEXT,
            lat REAL NOT NULL,
            lng REAL NOT NULL,
            antipode_name TEXT,
            antipode_name_en TEXT,
            antipode_lat REAL,
            antipode_lng REAL,
            category TEXT DEFAULT '陆-陆',
            precision_level TEXT DEFAULT '区域级',
            deviation_km REAL,
            poem TEXT,
            is_active INTEGER DEFAULT 1,
            created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
            updated_at DATETIME DEFAULT CURRENT_TIMESTAMP
        )
    `);

    db.run(`CREATE UNIQUE INDEX IF NOT EXISTS idx_cities_name_cn ON cities(name_cn)`);

    db.run(`
        CREATE TABLE IF NOT EXISTS city_images (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            city_id INTEGER NOT NULL,
            image_type TEXT NOT NULL,
            image_path TEXT NOT NULL,
            FOREIGN KEY (city_id) REFERENCES cities(id)
        )
    `);

    db.run(`
        CREATE TABLE IF NOT EXISTS gifts (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            name_cn TEXT NOT NULL,
            name_en TEXT,
            description TEXT,
            country TEXT,
            category TEXT,
            image_ripe TEXT,
            image_green TEXT,
            poem_ripe TEXT,
            poem_green TEXT,
            has_green INTEGER DEFAULT 0,
            is_active INTEGER DEFAULT 1,
            created_at DATETIME DEFAULT CURRENT_TIMESTAMP
        )
    `);

    db.run(`
        CREATE TABLE IF NOT EXISTS city_gifts (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            city_id INTEGER NOT NULL,
            gift_id INTEGER NOT NULL,
            FOREIGN KEY (city_id) REFERENCES cities(id),
            FOREIGN KEY (gift_id) REFERENCES gifts(id),
            UNIQUE(city_id, gift_id)
        )
    `);

    db.run(`
        CREATE TABLE IF NOT EXISTS search_logs (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            city_name TEXT,
            city_id INTEGER,
            ip TEXT,
            user_agent TEXT,
            created_at DATETIME DEFAULT CURRENT_TIMESTAMP
        )
    `);

    db.run(`
        CREATE TABLE IF NOT EXISTS gift_logs (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            city_id INTEGER,
            gift_id INTEGER,
            state TEXT,
            ip TEXT,
            created_at DATETIME DEFAULT CURRENT_TIMESTAMP
        )
    `);

    db.run(`
        CREATE TABLE IF NOT EXISTS settings (
            key TEXT PRIMARY KEY,
            value TEXT,
            updated_at DATETIME DEFAULT CURRENT_TIMESTAMP
        )
    `);

    console.log('✅ 数据表创建完成');
});

